export function getAircraftType(id) {
  return AIRCRAFT_TYPES.find(a => a.id === id);
}

export const AIRCRAFT_CATEGORIES = ['Turboprop', 'Regional Jet', 'Narrow Body', 'Wide Body', 'Double Deck', 'Supersonic', 'Freighter'];

// ─── Purchase pricing helpers ─────────────────────────────────────────────────

/**
 * Fleet-commonality discount on purchase price.
 * Each additional unit of the same type you already own earns a slightly better deal.
 * alreadyOwned = units of this type in your fleet before this purchase.
 */
export function buyDiscount(alreadyOwned) {
  if (alreadyOwned >= 4) return 0.05;   // 5% — large fleet order
  if (alreadyOwned >= 2) return 0.03;   // 3% — growing fleet
  if (alreadyOwned >= 1) return 0.02;   // 2% — second unit
  return 0;
}

/**
 * Actual price after fleet discount.
 * @param {object} type          - aircraft type object from AIRCRAFT_TYPES
 * @param {number} alreadyOwned  - units already in fleet
 */
export function effectivePurchasePrice(type, alreadyOwned) {
  return Math.round(type.purchasePrice * (1 - buyDiscount(alreadyOwned)));
}

// ─── Efficiency metric ────────────────────────────────────────────────────────

/**
 * Fuel cost per seat per 100 km (¢/seat/100km).
 * Lower = more efficient. Use for comparison across aircraft sizes.
 */
export function seatEfficiency(type) {
  // Freighters have no seats — seat efficiency is undefined for them. Return null so
  // they're excluded from the passenger efficiency comparison rather than producing
  // Infinity and corrupting the fleet-wide bounds below.
  if (type.freighter || !type.seats) return null;
  return (fuelCostPerKm(type) / type.seats) * 100;
}

// Bounds across the passenger fleet — used to normalise efficiency bars in the UI.
// Freighters are excluded (seatEfficiency returns null for them).
const _effValues = AIRCRAFT_TYPES.map(t => seatEfficiency(t)).filter(v => v != null);
export const EFFICIENCY_BEST  = Math.min(..._effValues); // lowest $/seat/100km
export const EFFICIENCY_WORST = Math.max(..._effValues); // highest $/seat/100km

/**
 * Normalised efficiency score 0–100 (100 = most efficient in the fleet).
 */
export function efficiencyScore(type) {
  const e = seatEfficiency(type);
  if (e == null) return null;  // freighters have no seat-efficiency score
  return Math.round(
    ((EFFICIENCY_WORST - e) / (EFFICIENCY_WORST - EFFICIENCY_BEST)) * 100
  );
}
